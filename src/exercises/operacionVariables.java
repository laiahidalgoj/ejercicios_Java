package fran;

public class operacionVariables {

    public static void main(String[] args) {

        int numero1 = 20;
        int numero2 = 10;

        int suma = numero1 + numero2;
        int multiplicacion = numero1 * numero2;
        double division = numero1 / numero2;
        double modulo = numero1 % numero2;
        int resta = numero1 - numero2;

        System.out.println("La suma de la operación es: " + suma);
        System.out.println("La multiplicacion de la operación es: " + multiplicacion);
        System.out.println("La división de la operación es: " + division);
        System.out.println("El módulo de la operación es: " + modulo);
        System.out.println("La resta de la operación es: " + resta);



    }
}
